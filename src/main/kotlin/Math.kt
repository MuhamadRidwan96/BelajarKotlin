fun main() {
    val result = 10.0 / 3.0
    println(result)

    val result2 = 10 + 10 /2
    println(result2)

    var result3 = 0

    val barang1 = 100
    result3 += barang1

    val barang2 = 50
    result3 += barang2

    val barang3 = 20
    result3 += barang3

    println(result3)
    result3++
    println(result3)
    result3--
    println(result3)

    val sakit = true
    println(!sakit)

    /*
    Augmented Assignment
    * a = a + 10 <==> a += 10
    * a = a - 10 <==> a -= 10
    * a = a * 10 <==> a *= 10
    * a = a / 10 <==> a /= 10
    * a = a % 10 <==> a %= 10
    * */

    /*Unary Operator
    * ++ <==> a = a+1
    * -- <==> a = a-1
    * + == positive
    * - == negative
    * ! ==  reverse boolean
    * */
}